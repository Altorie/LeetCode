public class RecentCounter {
    Deque<Integer> queue = new ArrayDeque<>();
    public RecentCounter() {

    }

    public int ping(int t) {
        queue.addLast(t);
        while (queue.getFirst() < t-3000){
            queue.removeFirst();
        }
        return queue.size();
    }
}
